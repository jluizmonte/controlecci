    private void carregarRegistro() {
        colorirLinhas();
        jlQtde.setText(alunoController.retornaQtdeAluno());
        listaAlunoModels = alunoController.getListaAlunoCadastro();
        DefaultTableModel modeloTabela = (DefaultTableModel) jtAlunos.getModel();
        TableColumnModel tcm = jtAlunos.getColumnModel();
        tcm.getColumn(0).setPreferredWidth(2);
        tcm.getColumn(3).setPreferredWidth(2);
        tcm.getColumn(4).setPreferredWidth(5);

        modeloTabela.setNumRows(0);
        try {
            int cont = listaAlunoModels.size();
            for (int i = 0; i < cont; i++) {
                modeloTabela.addRow(new Object[]{
                    listaAlunoModels.get(i).getMatricula(),
                    listaAlunoModels.get(i).getNome(),
                    listaAlunoModels.get(i).getCurso(),
                    listaAlunoModels.get(i).getDataMatricula(),
                    listaAlunoModels.get(i).getSituacao()
                });
            }
            chamarJDialog();
        } catch (Exception e) {
            e.toString();
            LocalUtil.logClass = this.getClass().getName();
            LocalUtil.logType = templateAlerts.mensagemRegsitroErro();
            new LogCatUtil().writeFile(String.valueOf(templateAlerts.erroBuscarDados("Aluno")));
        }
    }

